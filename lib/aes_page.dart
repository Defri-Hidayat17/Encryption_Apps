  @override
  State<AesPage> createState() => _AesPageState();
}

enum CipherMode { encrypt, decrypt }

class _AesPageState extends State<AesPage> {
  // Controllers untuk input kunci, IV, teks input, dan output
  final TextEditingController _keyController = TextEditingController();
  final TextEditingController _ivController = TextEditingController();
  final TextEditingController _messageController = TextEditingController();
  final TextEditingController _cipherInputController = TextEditingController();

  String _result = ''; // Hasil akhir enkripsi/dekripsi

  CipherMode _currentMode = CipherMode.encrypt; // Mode default
  String _errorMessage = ''; // Untuk menampilkan pesan error
  bool _isAutoGenerated = false; // Menandakan apakah kunci/IV dibuat otomatis

  // Keys for SharedPreferences
  static const String _aesKey = 'aesKey';
  static const String _aesIv = 'aesIv';
  static const String _aesMessage = 'aesMessage';
  static const String _aesCipherInput = 'aesCipherInput';
  static const String _aesCurrentMode = 'aesCurrentMode';
  static const String _aesIsAutoGenerated = 'aesIsAutoGenerated';

  @override
  void initState() {
    super.initState();
    SystemChrome.setSystemUIOverlayStyle(
      SystemUiOverlayStyle.dark.copyWith(
        statusBarIconBrightness: Brightness.dark,
        statusBarBrightness: Brightness.light,
        statusBarColor: Colors.white,
      ),
    );
    _loadData();

    _keyController.addListener(_saveData);
    _ivController.addListener(_saveData);
    _messageController.addListener(_saveData);
    _cipherInputController.addListener(_saveData);
  }

  @override
  void dispose() {
    _keyController.removeListener(_saveData);
    _ivController.removeListener(_saveData);
    _messageController.removeListener(_saveData);
    _cipherInputController.removeListener(_saveData);

    _keyController.dispose();
    _ivController.dispose();
    _messageController.dispose();
    _cipherInputController.dispose();
    super.dispose();
  }

  // ============================
  // üíæ Fungsi untuk menyimpan data ke SharedPreferences
  // ============================
  Future<void> _saveData() async {
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString(_aesKey, _keyController.text);
    await prefs.setString(_aesIv, _ivController.text);
    await prefs.setString(_aesMessage, _messageController.text);
    await prefs.setString(_aesCipherInput, _cipherInputController.text);
    await prefs.setInt(_aesCurrentMode, _currentMode.index);
    await prefs.setBool(_aesIsAutoGenerated, _isAutoGenerated);
  }

  // ============================
  // üì• Fungsi untuk memuat data dari SharedPreferences
  // ============================
  Future<void> _loadData() async {
    final prefs = await SharedPreferences.getInstance();
    setState(() {
      _keyController.text = prefs.getString(_aesKey) ?? '';
      _ivController.text = prefs.getString(_aesIv) ?? '';
      _messageController.text = prefs.getString(_aesMessage) ?? '';
      _cipherInputController.text = prefs.getString(_aesCipherInput) ?? '';
      _currentMode = CipherMode.values[prefs.getInt(_aesCurrentMode) ?? 0];
      _isAutoGenerated = prefs.getBool(_aesIsAutoGenerated) ?? false;

      // Re-calculate result if keys/IV and messages are present
      // Only process silently if there's actual data to process
      if (_keyController.text.isNotEmpty && _ivController.text.isNotEmpty) {
        if (_currentMode == CipherMode.encrypt &&
            _messageController.text.isNotEmpty) {
          _processCipher(silent: true);
        } else if (_currentMode == CipherMode.decrypt &&
            _cipherInputController.text.isNotEmpty) {
          _processCipher(silent: true);
        }
      }
    });
  }

  // ============================
  // üîÑ Fungsi untuk mengosongkan semua input dan hasil
  // ============================
  void _clearAll() {
    setState(() {
      _keyController.clear();
      _ivController.clear();
      _messageController.clear();
      _cipherInputController.clear();
      _result = '';
      _currentMode = CipherMode.encrypt;
      _errorMessage = '';
      _isAutoGenerated = false;
    });
    _saveData();
    if (mounted) {
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(
          content: Text('Semua input dan hasil telah dikosongkan!'),
        ),
      );
    }
  }

  // ========================================
  // üîë Key and IV Generation
  // ========================================
  void _generateKeyAndIV({bool silent = false}) {
    setState(() {
      _errorMessage = '';
      _isAutoGenerated = true; // Set to true when auto-generating
    });

    try {
      // Generate a random 256-bit (32-byte) key
      final keyBytes = encrypt.Key.fromSecureRandom(32);
      _keyController.text = base64.encode(keyBytes.bytes);

      // Generate a random 128-bit (16-byte) IV
      final ivBytes = encrypt.IV.fromSecureRandom(16);
      _ivController.text = base64.encode(ivBytes.bytes);

      _saveData();
      if (!silent && mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(
            content: Text('Kunci dan IV AES otomatis berhasil dibuat!'),
          ),
        );
      }
    } catch (e) {
      if (!silent) {
        setState(() {
          _errorMessage = 'Gagal membuat kunci/IV otomatis: $e';
        });
      }
    }
  }

  // ========================================
  // üîí Enkripsi AES
  // ========================================
  String _encryptMessage(String plaintext) {
    if (_keyController.text.isEmpty || _ivController.text.isEmpty) {
      setState(() {
        _errorMessage = 'Kunci dan IV harus diisi.';
      });
      return '';
    }

    try {
      final keyBytes = base64.decode(_keyController.text);
      final ivBytes = base64.decode(_ivController.text);

      if (keyBytes.length != 32) {
        // AES-256 requires 32 bytes (256 bits)
        setState(() {
          _errorMessage = 'Panjang kunci harus 32 byte (256 bit) Base64.';
        });
        return '';
      }
      if (ivBytes.length != 16) {
        // AES block size is 16 bytes (128 bits)
        setState(() {
          _errorMessage = 'Panjang IV harus 16 byte (128 bit) Base64.';
        });
        return '';
      }

      final key = encrypt.Key(keyBytes);
      final iv = encrypt.IV(ivBytes);

      final encrypter = encrypt.Encrypter(
        encrypt.AES(key, mode: encrypt.AESMode.cbc, padding: 'PKCS7'),
      );

      final encrypted = encrypter.encrypt(plaintext, iv: iv);
      return encrypted.base64;
    } catch (e) {
      setState(() {
        _errorMessage =
            'Gagal enkripsi: Pastikan kunci dan IV valid Base64. $e';
      });
      return '';
    }
  }

  // ========================================
  // üîì Dekripsi AES
  // ========================================
  String _decryptMessage(String ciphertext) {
    if (_keyController.text.isEmpty || _ivController.text.isEmpty) {
      setState(() {
        _errorMessage = 'Kunci dan IV harus diisi.';
      });
      return '';
    }

    try {
      final keyBytes = base64.decode(_keyController.text);
      final ivBytes = base64.decode(_ivController.text);

      if (keyBytes.length != 32) {
        setState(() {
          _errorMessage = 'Panjang kunci harus 32 byte (256 bit) Base64.';
        });
        return '';
      }
      if (ivBytes.length != 16) {
        setState(() {
          _errorMessage = 'Panjang IV harus 16 byte (128 bit) Base64.';
        });
        return '';
      }

      final key = encrypt.Key(keyBytes);
      final iv = encrypt.IV(ivBytes);

      final encrypter = encrypt.Encrypter(
        encrypt.AES(key, mode: encrypt.AESMode.cbc, padding: 'PKCS7'),
      );

      final decrypted = encrypter.decrypt64(ciphertext, iv: iv);
      return decrypted;
    } catch (e) {
      setState(() {
        _errorMessage =
            'Gagal dekripsi: Pastikan kunci, IV, dan ciphertext valid Base64. $e';
      });
      return '';
    }
  }

  // ========================================
  // ‚öôÔ∏è Proses Enkripsi/Dekripsi Utama
  // ========================================
  void _processCipher({bool silent = false}) {
    setState(() {
      _errorMessage = '';
    });

    if (_keyController.text.isEmpty || _ivController.text.isEmpty) {
      if (!silent) {
        setState(() {
          _errorMessage =
              'Harap masukkan atau buat kunci dan IV AES terlebih dahulu.';
        });
      }
      return;
    }

    if (_currentMode == CipherMode.encrypt) {
      final message = _messageController.text.trim();
      if (message.isEmpty) {
        if (!silent) {
          setState(() {
            _errorMessage = 'Masukkan teks untuk dienkripsi!';
          });
        }
        return;
      }
      final encrypted = _encryptMessage(message);
      if (encrypted.isNotEmpty) {
        setState(() {
          _result = encrypted;
          _cipherInputController.text = encrypted;
        });
      }
    } else {
      // CipherMode.decrypt
      final cipherInput = _cipherInputController.text.trim();
      if (cipherInput.isEmpty) {
        if (!silent) {
          setState(() {
            _errorMessage = 'Masukkan teks terenkripsi untuk didekripsi!';
          });
        }
        return;
      }
      final decrypted = _decryptMessage(cipherInput);
      if (decrypted.isNotEmpty) {
        setState(() {
          _result = decrypted;
          _messageController.text = decrypted;
        });
      }
    }
    _saveData();
    if (!silent && _errorMessage.isEmpty && mounted) {
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(
          content: Text(
            _currentMode == CipherMode.encrypt
                ? 'Pesan berhasil dienkripsi!'
                : 'Pesan berhasil didekripsi!',
          ),
        ),
      );
    }
  }

